  setLanguage(language: Language): void {
    this._currentLanguage.set(language);
    localStorage.setItem(this.LANGUAGE_KEY, language);
  }
  
  getCurrentLanguageOption(): LanguageOption {
    const currentLang = this._currentLanguage();
    return this.availableLanguages.find(lang => lang.code === currentLang) || this.availableLanguages[0];
  }
  
  translate(key: string, params?: Record<string, string>): string {
    const currentLang = this._currentLanguage();
    let translation = this.translations[currentLang]?.[key] || this.translations['en'][key] || key;
    
    // Replace parameters if provided
    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        translation = translation.replace(`{{${param}}}`, value);
      });
    }
    
    return translation;
  }
  
  // Convenience method for template usage
  t(key: string, params?: Record<string, string>): string {
    return this.translate(key, params);
  }
  
