  private mergeTranslations(translations: Record<string, Record<string, string>>): void {
    Object.keys(translations).forEach(lang => {
      if (this.translations[lang as Language]) {
        this.translations[lang as Language] = {
          ...this.translations[lang as Language],
          ...translations[lang]
        };
      }
    });
  }
  
  private getInitialLanguage(): Language {
    // 1. Check localStorage first (user preference has highest priority)
    const savedLanguage = localStorage.getItem(this.LANGUAGE_KEY) as Language;
    if (savedLanguage && this.availableLanguages.some(lang => lang.code === savedLanguage)) {
      return savedLanguage;
    }
    
    // 2. Detect region and set appropriate language
    if (typeof navigator !== 'undefined') {
      const detectedLanguage = this.detectLanguageFromRegion();
      if (detectedLanguage) {
        return detectedLanguage;
      }
    }
    
    // 3. Default fallback to English
    return 'en';
  }

